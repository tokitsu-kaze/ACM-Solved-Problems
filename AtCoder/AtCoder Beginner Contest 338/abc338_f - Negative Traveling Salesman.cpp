		for(s=0;s<(1<<n);s++)
		{
			for(j=0;j<n;j++)
			{
				if(!(s&(1<<j))) continue;
				if(dp[s][j]==INF) continue;
				for(k=0;k<n;k++)
				{
					if(flyd.dis[j][k]==flyd.inf) continue;
					dp[s|(1<<k)][k]=min(dp[s|(1<<k)][k],dp[s][j]+flyd.dis[j][k]);
				}
			}
		}
