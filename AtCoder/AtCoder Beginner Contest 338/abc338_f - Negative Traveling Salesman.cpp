struct Floyd
{
	#define type int
	const type inf=INF;
	static const int N=21;
	int n;
	type mp[N][N],dis[N][N];
	void init(int _n)
	{
		int i,j;
		n=_n;
		for(i=0;i<=n;i++)
		{
			for(j=0;j<=n;j++)
			{
				mp[i][j]=dis[i][j]=inf;
			}
		}
	}
	void add_edge(int x,int y,type w)
	{
		w=min(mp[x][y],w);
		mp[x][y]=dis[x][y]=w;
	}
	void work()
	{
		int i,j,k;
		for(k=0;k<n;k++)
		{
			for(i=0;i<n;i++)
			{
				if(dis[i][k]==inf) continue;
				for(j=0;j<n;j++)
				{
					if(dis[k][j]==inf) continue;
					dis[i][j]=min(dis[i][j],dis[i][k]+dis[k][j]);
				}
			}
		}
	}
	#undef type
}flyd;
