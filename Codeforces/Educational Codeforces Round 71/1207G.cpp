void dfs(int x,int now)
{
	now=ac.nex[now][ac.getid(c[x])];
	tr.insert(ac.l[now],1);
	for(auto to:mp[x]) dfs(to,now);
	for(auto it:qst[x]) ans[it.se]=tr.ask(ac.l[it.fi],ac.r[it.fi]);
	tr.insert(ac.l[now],-1);
}
