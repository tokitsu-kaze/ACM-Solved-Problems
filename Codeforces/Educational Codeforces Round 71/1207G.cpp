struct Fenwick_Tree
{
	#define type int
	type bit[MAX];
	int n;
	void init(int _n){n=_n;mem(bit,0);}
	int lowbit(int x){return x&(-x);}
	void insert(int x,type v)
	{
		while(x<=n)
		{
			bit[x]+=v;
			x+=lowbit(x);
		}
	}
	type get(int x)
	{
		type res=0;
		while(x)
		{
			res+=bit[x];
			x-=lowbit(x);
		}
		return res;
	}
	type ask(int l,int r)
	{
		if(l-1<=0) return get(r);
		return get(r)-get(l-1);
	}
	#undef type
}tr;
struct AC_Automaton
{
	static const int K=26;//may need change
	int nex[MAX][K],fail[MAX],cnt[MAX],last[MAX];
	int root,tot;
	inline int getid(char c){return c-'a';}//may need change
	int newnode()
	{
		mem(nex[tot],0);
		fail[tot]=0;
		cnt[tot]=0;
		return tot++;
	}
	void init()
	{
		tot=0;
		root=newnode();
	}
	int insert(char *s)
	{
		int len,now,i;
		len=strlen(s);
		now=root;
		for(i=0;i<len;i++)
		{
			int t=getid(s[i]);
			if(!nex[now][t]) nex[now][t]=newnode();
			now=nex[now][t];
		}
		cnt[now]++;
		return now;
	}
	void work()
	{
		int i,now;
		queue<int>q;
		for(i=0;i<K;i++)
		{
			if(nex[root][i]) q.push(nex[root][i]);
		}
		while(!q.empty())
		{
			now=q.front();
			q.pop();
			if(cnt[fail[now]]) last[now]=fail[now];
			else last[now]=last[fail[now]];
			for(i=0;i<K;i++)
			{
				if(nex[now][i])
				{
					fail[nex[now][i]]=nex[fail[now]][i];
					q.push(nex[now][i]);
				}
				else nex[now][i]=nex[fail[now]][i];
			}
		}
	}
	vector<int> mp[MAX];
	int l[MAX],r[MAX],tt;
	void dfs(int x)
	{
		l[x]=++tt;
		for(auto to:mp[x]) dfs(to);
		r[x]=tt;
	}
	void build_tree()
	{
		for(int i=0;i<=tot;i++) mp[i].clear();
		for(int i=1;i<tot;i++) mp[fail[i]].pb(i);
		tt=0;
		dfs(root);
	}
	
}ac;
int op[MAX],ans[MAX];
