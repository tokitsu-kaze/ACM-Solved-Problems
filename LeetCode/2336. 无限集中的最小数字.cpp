set<int> s;
int now;

class SmallestInfiniteSet {
public:
    SmallestInfiniteSet() {
		s.clear();
		s.insert(INF);
		now=0;
    }
    
    int popSmallest() {
		if(*s.begin()<now+1)
		{
			int res=*s.begin();
			s.erase(*s.begin());
			return res;
		}
		else return ++now;
    }
    
    void addBack(int num) {
		if(num<now+1) s.insert(num);
    }
};

