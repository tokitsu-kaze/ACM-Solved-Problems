/*********************************  head  *********************************/
struct Fenwick_Tree
{
	#define type int
	type bit[MAX];
	int n;
	void init(int _n)
	{
		n=_n;
		for(int i=0;i<=n;i++) bit[i]=0;
	}
	int lowbit(int x){return x&(-x);}
	void insert(int x,type v)
	{
		while(x<=n)
		{
			bit[x]+=v;
			x+=lowbit(x);
		}
	}
	type get(int x)
	{
		type res=0;
		while(x)
		{
			res+=bit[x];
			x-=lowbit(x);
		}
		return res;
	}
	type ask(int l,int r)
	{
		if(l-1<=0) return get(r);
		return get(r)-get(l-1);
	}
	#undef type
}tr;
class Solution {
public:
    int countSubarrays(vector<int>& nums, int k) {
        ll ans=0;
        tr.init(MAX-5);
        tr.insert(100001,1);
        int now=0;
        for(auto it:nums)
        {
        	if(it<k) now--;
        	else now++;
        	tr.insert(100001+now,1);
        	ans+=tr.ask(1,100001+now-1);
        	
		}
		
		tr.init(MAX-5);
		tr.insert(100001,1);
		now=0;
        for(auto it:nums)
        {
        	if(it<k+1) now--;
        	else now++;
        	tr.insert(100001+now,1);
        	ans-=tr.ask(1,100001+now-1);
		}
		return ans;
    }
};

