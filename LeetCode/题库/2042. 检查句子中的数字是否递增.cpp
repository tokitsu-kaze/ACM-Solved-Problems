class Solution {
public:
    bool areNumbersAscending(string s) {
    	string tmp;
    	int pre=-1;
    	int now;
    	s+=' ';
        for(auto &ch:s)
        {
        	if(ch==' ')
        	{
        		now=0;
        		for(auto &it:tmp)
        		{
        			if(it>='0'&&it<='9') now=now*10+(it-'0');
        			else
        			{
        				now=-1;
        				break;
					}
				}
				if(now!=-1)
				{
					if(now>pre) pre=now;
					else return false;
				}
				tmp="";
			}
			else tmp+=ch;
		}
		return true;
		
    }
};
