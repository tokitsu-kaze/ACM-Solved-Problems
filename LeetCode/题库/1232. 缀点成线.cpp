class Solution {
public:
    bool checkStraightLine(vector<vector<int>>& coordinates) {
    	if(sz(coordinates)<=2) return 1;
		sort(all(coordinates));
		int g,dx,dy,x,y,i;
		g=__gcd(coordinates[1][0]-coordinates[0][0],
				coordinates[1][1]-coordinates[0][1]);
		dx=(coordinates[1][0]-coordinates[0][0])/g;
		dy=(coordinates[1][1]-coordinates[0][1])/g;
		for(i=2;i<sz(coordinates);i++)
		{
			g=__gcd(coordinates[i][0]-coordinates[0][0],
					coordinates[i][1]-coordinates[0][1]);
			x=(coordinates[i][0]-coordinates[0][0])/g;
			y=(coordinates[i][1]-coordinates[0][1])/g;
			if(x!=dx||y!=dy) return 0;
		}
		return 1;
    }
};
