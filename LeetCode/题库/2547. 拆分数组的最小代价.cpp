/*********************************  head  *********************************/
int dp[MAX];
class Solution {
public:
    int minCost(vector<int>& nums, int k) {
        mem(dp,0x3f);
		dp[0]=0;
		int i,j,n,now;
		n=sz(nums);
		for(i=1;i<=n;i++)
		{
			gp_hash_table<int,int> mp;
			now=0;
			for(j=i;j;j--)
			{
				mp[nums[j-1]]++;
				if(mp[nums[j-1]]==2) now+=2;
				else if(mp[nums[j-1]]>2) now++;
				dp[i]=min(dp[i],dp[j-1]+k+now);
			}
		}
		return dp[n];
    }
};
