/*********************************  head  *********************************/
class Solution {
public:
    void setZeroes(vector<vector<int>>& matrix) {
		int i,j,n,m,fx[205],fy[205];
		n=sz(matrix);
		m=sz(matrix[0]);
		mem(fx,0);
		mem(fy,0);
		for(i=0;i<n;i++)
		{
			for(j=0;j<m;j++)
			{
				if(matrix[i][j]==0) fx[i]=fy[j]=1;
			}
		}
		for(i=0;i<n;i++)
		{
			if(!fx[i]) continue;
			for(j=0;j<m;j++) matrix[i][j]=0;
		}
		for(j=0;j<m;j++)
		{
			if(!fy[j]) continue;
			for(i=0;i<n;i++) matrix[i][j]=0;
		}
    }
};
