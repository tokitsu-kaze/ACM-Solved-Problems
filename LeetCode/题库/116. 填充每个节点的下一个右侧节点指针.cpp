vector<Node *> res[22];
void dfs(Node *x,int h)
{
	if(!x) return;
	res[h].pb(x);
	dfs(x->left,h+1);
	dfs(x->right,h+1);
}
class Solution {
public:
    Node* connect(Node* root) {
    	int i,j;
		for(i=0;i<22;i++) res[i].clear();
        dfs(root,0);
        for(i=0;i<22;i++)
        {
        	for(j=1;j<sz(res[i]);j++)
        	{
        		res[i][j-1]->next=res[i][j];
			}
		}
		return root;
    }
};
