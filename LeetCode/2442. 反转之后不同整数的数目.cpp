class Solution {
public:
    int countDistinctIntegers(vector<int>& nums) {
        set<string> s;
        for(auto it:nums)
        {
        	string tmp=to_string(it);
        	s.insert(tmp);
        	reverse(all(tmp));
        	string tmp2;
        	int p=0;
        	while(p<sz(tmp)&&tmp[p]=='0') p++;
        	for(;p<sz(tmp);p++) tmp2+=tmp[p];
        	s.insert(tmp2);
		}
		return sz(s);
    }
};
