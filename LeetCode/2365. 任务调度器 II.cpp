class Solution {
public:
    long long taskSchedulerII(vector<int>& tasks, int space) {
        ll res=0;
        map<int,ll> mp;
        for(auto it:tasks)
        {
			res++;
			if(!mp.count(it)) mp[it]=res;
			else if(res-mp[it]-1>=space) mp[it]=res;
			else
			{
				res=mp[it]+space+1;
				mp[it]=res;
			}
		}
		return res;
    }
};
