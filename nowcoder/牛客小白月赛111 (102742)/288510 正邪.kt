fun go()
{
	var n=readint()
	require(n>=3 && n<=1e6)
	var s=readstring()
	require(s.length==n)
	var ans=0
	var now=0
	for(it in s)
	{
		require(it=='y' || it=='n')
		if(it=='y')
		{
			ans++
			ans+=now/2
			now=0
		}
		else now++
	}
	ans+=now/2
